CREATE PROCEDURE `func_matchassetdirectory`(
	IN `t` varchar(512),
	IN `p` varchar(512),
	IN `l` varchar(512)
)
begin
	set @stmt = concat('SELECT * FROM assetdirectory WHERE id IN (SELECT id_assetdirectory FROM autoarchivescheme WHERE id IN (select t2.* from (SELECT ass_autoarchivescheme_assettype.id_autoarchivescheme FROM (ass_autoarchivescheme_assettype LEFT JOIN ass_autoarchivescheme_project ON ass_autoarchivescheme_project.id_autoarchivescheme IS NULL OR ass_autoarchivescheme_project.id_autoarchivescheme = ass_autoarchivescheme_assettype.id_autoarchivescheme) LEFT JOIN ass_autoarchivescheme_assetlabel ON ass_autoarchivescheme_assetlabel.id_autoarchivescheme IS NULL OR ass_autoarchivescheme_assetlabel.id_autoarchivescheme = ass_autoarchivescheme_assettype.id_autoarchivescheme WHERE id_assettype ',t,' AND id_project ',p,' AND id_assetlabel ',l,' GROUP BY id_autoarchivescheme ORDER BY COUNT(*) DESC LIMIT 3) as t2));');
	prepare stmt from @stmt;
    execute stmt;
    deallocate prepare stmt;
end//
